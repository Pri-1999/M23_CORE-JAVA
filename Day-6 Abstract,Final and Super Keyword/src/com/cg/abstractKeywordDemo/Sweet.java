package com.cg.abstractKeywordDemo;

public class Sweet extends Rasmalai
{
	public Sweet()
	{
		System.out.println("I Like Rasmalai");
	}
	public String taste()
	{
		return "Sweet";
	}
	// Implementation Of abstract method
	void print()
	{
		System.out.println("Sweet is Rasmalai");
	}

}
